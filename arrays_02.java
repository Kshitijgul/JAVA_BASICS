package Bootcamp;

public class arrays_02 {
    public static void sum(int a, int b) {
        System.out.println(a + b);
    }

    public static void main(String[] args) {
        int a = 12;
        int b = 16;
        int sum = a + b;
        System.out.println(sum);
        // With the Help of Function
        sum(40, 25);
        sum(40, 25);
    }
}
